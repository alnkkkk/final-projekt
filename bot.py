
import logging # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫—É –¥–ª—è –≤–µ–¥–µ–Ω–∏—è –∂—É—Ä–Ω–∞–ª–∞ –æ—à–∏–±–æ–∫ –∏ —Å–æ–æ–±—â–µ–Ω–∏–π
import os # –ú–æ–¥—É–ª—å –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω–æ–π —Å–∏—Å—Ç–µ–º–æ–π (—á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å —Ç–æ–∫–µ–Ω –±–æ—Ç–∞)

from dotenv import load_dotenv # –ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —á—Ç–µ–Ω–∏—è –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–∑ —Ñ–∞–π–ª–∞ .env
from telegram import Update, InlineKeyboardButton, InlineKeyboardMarkup # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω—É–∂–Ω—ã–µ –∫–ª–∞—Å—Å—ã –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –¢–µ–ª–µ–≥—Ä–∞–º API
from telegram.ext import (  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å Telegram Bot API
    ApplicationBuilder,
    CommandHandler,
    MessageHandler,
    ContextTypes,
    filters,
)

from kino_client import extract_kp_id, get_movie_info # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –∫–∏–Ω–æ–∑–∞–ø—Ä–æ—Å–∞–º–∏
from storage import init_db, save_request  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –±–∞–∑–æ–π –¥–∞–Ω–Ω—ã—Ö


logging.basicConfig(  # –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º –≤–µ–¥–µ–Ω–∏–µ –ª–æ–≥–æ–≤
    format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
    level=logging.INFO,  # –£—Ä–æ–≤–µ–Ω—å –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –Ω–∞ INFO
)
logger = logging.getLogger(__name__) # –°–æ–∑–¥–∞–µ–º —ç–∫–∑–µ–º–ø–ª—è—Ä –ª–æ–≥–µ—Ä–∞ –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ –º–æ–¥—É–ª—è



load_dotenv() # –ß–∏—Ç–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–∑ —Ñ–∞–π–ª–∞ .env
BOT_TOKEN = os.getenv("BOT_TOKEN") # –ü–æ–ª—É—á–∞–µ–º —Ç–æ–∫–µ–Ω –±–æ—Ç–∞ –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è



async def start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None: # –ö–æ–º–∞–Ω–¥–∞ /start
    text = (  # –°–æ–æ–±—â–µ–Ω–∏–µ –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏—è –∏ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        "–ü—Ä–∏–≤–µ—Ç! –Ø –±–æ—Ç, –∫–æ—Ç–æ—Ä—ã–π –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç —Ä–µ–π—Ç–∏–Ω–≥ —Ñ–∏–ª—å–º–∞ –Ω–∞ –ö–∏–Ω–æ–ø–æ–∏—Å–∫–µ.\n\n"
        "–ü—Ä–∏—à–ª–∏ –º–Ω–µ —Å—Å—ã–ª–∫—É –≤–∏–¥–∞:\n"
        "https://www.kinopoisk.ru/film/535341/\n\n"
        "–ö–æ–º–∞–Ω–¥—ã:\n"
        "/start ‚Äî –Ω–∞—á–∞—Ç—å\n"
        "/help ‚Äî —Å–ø—Ä–∞–≤–∫–∞\n"
        "/stats ‚Äî —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –∑–∞–ø—Ä–æ—Å–æ–≤"
    )
    await update.message.reply_text(text)  # –û—Ç–≤–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é —Å –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è–º–∏


async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None: # –ö–æ–º–∞–Ω–¥–∞ /help
    await start(update, context) # –ü—Ä–æ—Å—Ç–æ –ø–µ—Ä–µ–∞–¥—Ä–µ—Å–æ–≤—ã–≤–∞–µ–º –∫–æ–º–∞–Ω–¥—É /help –≤ /start


def format_movie_message(movie: dict) -> str: # –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ —Ñ–∏–ª—å–º–µ
    title = movie.get("title") or "–§–∏–ª—å–º" # –ü–æ–ª—É—á–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ —Ñ–∏–ª—å–º–∞ –∏–ª–∏ —Å—Ç–∞–≤–∏–º –¥–µ—Ñ–æ–ª—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
    year = movie.get("year") # –ü–æ–ª—É—á–∞–µ–º –≥–æ–¥ –≤—ã–ø—É—Å–∫–∞ —Ñ–∏–ª—å–º–∞
    rating = movie.get("rating") # –ü–æ–ª—É—á–∞–µ–º —Ä–µ–π—Ç–∏–Ω–≥ —Ñ–∏–ª—å–º–∞

    lines = [f"üé¨ {title}" + (f" ({year})" if year else "")] # –ù–∞—á–∏–Ω–∞–µ–º —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞—Ç—å —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –Ω–∞–∑–≤–∞–Ω–∏—è –∏ –≥–æ–¥–∞ –≤—ã–ø—É—Å–∫–∞
    if rating is not None: # –ï—Å–ª–∏ —Ä–µ–π—Ç–∏–Ω–≥ –¥–æ—Å—Ç—É–ø–µ–Ω
        lines.append(f"‚≠ê –†–µ–π—Ç–∏–Ω–≥ –ö–∏–Ω–æ–ø–æ–∏—Å–∫–∞: {rating}") # –î–æ–±–∞–≤–ª—è–µ–º —Ä–µ–π—Ç–∏–Ω–≥
        if rating >= 8.0:  # –õ–æ–≥–∏–∫–∞ –æ—Ü–µ–Ω–∫–∏ –∫–∞—á–µ—Å—Ç–≤–∞ —Ñ–∏–ª—å–º–∞
            lines.append("üî• –û–±—è–∑–∞—Ç–µ–ª–µ–Ω –∫ –ø—Ä–æ—Å–º–æ—Ç—Ä—É")
        elif rating >= 6.0:
            lines.append("üëç –ö—Ä–µ–ø–∫–∏–π —Ñ–∏–ª—å–º")
        else:
            lines.append("ü§∑‚Äç‚ôÇÔ∏è –ù–∞ –ª—é–±–∏—Ç–µ–ª—è")
    else:
        lines.append("–†–µ–π—Ç–∏–Ω–≥ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω")  # –ï—Å–ª–∏ —Ä–µ–π—Ç–∏–Ω–≥ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω, –æ—Ç–æ–±—Ä–∞–∂–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ


    return "\n".join(lines) # –û–±—ä–µ–¥–∏–Ω—è–µ–º —Å—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏ –≤ –µ–¥–∏–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ


async def handle_link(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None: # –û—Å–Ω–æ–≤–Ω–∞—è –ª–æ–≥–∏–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å—Å—ã–ª–æ–∫
    message = update.message # –ü–æ–ª—É—á–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    url = message.text.strip() # –û—á–∏—â–∞–µ–º —Å—Å—ã–ª–∫—É –æ—Ç –ø—Ä–æ–±–µ–ª–æ–≤

    await message.reply_text("‚è≥ –ò—â—É –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ñ–∏–ª—å–º–µ...")

    movie_id = extract_kp_id(url) # –ò–∑–≤–ª–µ–∫–∞–µ–º ID —Ñ–∏–ª—å–º–∞ –∏–∑ —Å—Å—ã–ª–∫–∏
    if not movie_id: # –ï—Å–ª–∏ ID –Ω–µ –∏–∑–≤–ª–µ—á—ë–Ω
        await message.reply_text( # –ü—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ –æ –Ω–µ–≤–µ—Ä–Ω–æ–π —Å—Å—ã–ª–∫–µ
            "‚ùå –ü–æ—Ö–æ–∂–µ, —ç—Ç–æ –Ω–µ —Å—Å—ã–ª–∫–∞ –Ω–∞ —Ñ–∏–ª—å–º –ö–∏–Ω–æ–ø–æ–∏—Å–∫–∞.\n"
            "–ü—Ä–∏—à–ª–∏, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ª–Ω—É—é —Å—Å—ã–ª–∫—É –≤–∏–¥–∞:\n"
            "https://www.kinopoisk.ru/film/326/"
        )
        return  # –ó–∞–≤–µ—Ä—à–∞–µ–º –æ–±—Ä–∞–±–æ—Ç–∫—É

    movie = get_movie_info(movie_id)  # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ñ–∏–ª—å–º–µ –ø–æ –µ–≥–æ ID
    if not movie: # –ï—Å–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
        await message.reply_text( # –°–æ–æ–±—â–∞–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –æ –ø—Ä–æ–±–ª–µ–º–∞—Ö
            "‚ö†Ô∏è –ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ñ–∏–ª—å–º–µ.\n"
            "–í–æ–∑–º–æ–∂–Ω–æ, —Å–µ—Ä–≤–∏—Å –≤—Ä–µ–º–µ–Ω–Ω–æ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω."
        )
        return

    # —Å–æ—Ö—Ä–∞–Ω—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
    save_request(
        user_id=message.from_user.id,  # –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π ID
        username=message.from_user.username,  # –ù–∏–∫–Ω–µ–π–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        movie_id=movie_id, # ID —Ñ–∏–ª—å–º–∞
        rating=movie.get("rating"), # –†–µ–π—Ç–∏–Ω–≥ —Ñ–∏–ª—å–º–∞
    )

    text = format_movie_message(movie)  # –§–æ—Ä–º–∏—Ä—É–µ–º –∫—Ä–∞—Å–∏–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ —Ñ–∏–ª—å–º–µ

    keyboard = [ # –°–æ–∑–¥–∞–µ–º –∫–ª–∞–≤–∏–∞—Ç—É—Ä—É —Å –∫–Ω–æ–ø–∫–æ–π –ø–µ—Ä–µ—Ö–æ–¥–∞ –Ω–∞ –ö–∏–Ω–æ–ø–æ–∏—Å–∫
        [
            InlineKeyboardButton(
                "üîó –û—Ç–∫—Ä—ã—Ç—å –Ω–∞ –ö–∏–Ω–æ–ø–æ–∏—Å–∫–µ", url=movie.get("url")
            )
        ]
    ]
    reply_markup = InlineKeyboardMarkup(keyboard) # –°–æ–∑–¥–∞–µ–º —Ä–∞–∑–º–µ—Ç–∫—É –∫–Ω–æ–ø–∫–∏

    await message.reply_text(text, reply_markup=reply_markup) # –ü—É–±–ª–∏–∫—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤–º–µ—Å—Ç–µ —Å –∫–Ω–æ–ø–∫–æ–π


async def stats(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None: # –ö–æ–º–∞–Ω–¥–∞ /stats
    from storage import get_stats  # –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏—é –ø–æ–ª—É—á–µ–Ω–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏

    total, top_movies = get_stats() # –ü–æ–ª—É—á–∞–µ–º –æ–±—â—É—é —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –∏ —Ç–æ–ø —Ñ–∏–ª—å–º–æ–≤
    lines = [f"–í—Å–µ–≥–æ –∑–∞–ø—Ä–æ—Å–æ–≤: {total}"] # –§–æ—Ä–º–∏—Ä—É–µ–º –Ω–∞—á–∞–ª—å–Ω—É—é —Å—Ç—Ä–æ–∫—É –æ—Ç—á–µ—Ç–∞
    if top_movies: # –ï—Å–ª–∏ –µ—Å—Ç—å —Ç–æ–ø —Ñ–∏–ª—å–º—ã 
        lines.append("\n–¢–æ–ø —Ñ–∏–ª—å–º–æ–≤ –ø–æ –∑–∞–ø—Ä–æ—Å–∞–º:") # –î–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ —Ä–∞–∑–¥–µ–ª–∞
        for title, count in top_movies: # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Å–ø–∏—Å–∫—É —Ç–æ–ø —Ñ–∏–ª—å–º–æ–≤
            lines.append(f"‚Ä¢ {title} ‚Äî {count}") # –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫–∏ —Å –∏–º–µ–Ω–µ–º —Ñ–∏–ª—å–º–∞ –∏ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ–º –∑–∞–ø—Ä–æ—Å–æ–≤
    else:
        lines.append("–ü–æ–∫–∞ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –Ω–µ—Ç ‚Äî —Å–¥–µ–ª–∞–π—Ç–µ –ø–µ—Ä–≤—ã–µ –∑–∞–ø—Ä–æ—Å—ã üôÇ") # –ï—Å–ª–∏ —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –ø–æ–∫–∞ –Ω–µ—Ç

    await update.message.reply_text("\n".join(lines))  # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ç—á–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é


def main() -> None: # –ì–ª–∞–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—É—Å–∫–∞ –±–æ—Ç–∞
    if not BOT_TOKEN: # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Ç–æ–∫–µ–Ω–∞
        raise RuntimeError("BOT_TOKEN –Ω–µ –∑–∞–¥–∞–Ω –≤ .env") # –ï—Å–ª–∏ —Ç–æ–∫–µ–Ω –Ω–µ —É–∫–∞–∑–∞–Ω, –≤—ã–±—Ä–∞—Å—ã–≤–∞–µ–º –æ—à–∏–±–∫—É

    init_db()  # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö (–µ—Å–ª–∏ —Ç–∞–±–ª–∏—Ü–∞ –µ—â–µ –Ω–µ —Å–æ–∑–¥–∞–Ω–∞)


    application = ( # –°–æ–∑–¥–∞–µ–º –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ Telegram Bot
        ApplicationBuilder()
        .token(BOT_TOKEN) # –ü—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º —Ç–æ–∫–µ–Ω –±–æ—Ç—É
        .build()
    )

    application.add_handler(CommandHandler("start", start)) # –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞ –∫–æ–º–∞–Ω–¥—ã /start
    application.add_handler(CommandHandler("help", help_command))  # –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞ –∫–æ–º–∞–Ω–¥—ã /help
    application.add_handler(CommandHandler("stats", stats)) # –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞ –∫–æ–º–∞–Ω–¥—ã /stats
    application.add_handler(
        MessageHandler(filters.TEXT & ~filters.COMMAND, handle_link) # –†–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∞ –≤—Ö–æ–¥—è—â–∏—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
    )

    application.run_polling()


if __name__ == "__main__":
    main()
